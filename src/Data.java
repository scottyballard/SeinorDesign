
public class Data {
	double date; 
	double value; 
	Data(double date, double value)
	{
		this.date=date;
		this.value=value;
	}
	public double getDate() {
		return date;
	}
	public void setDate(double date) {
		this.date = date;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
}
